"""Обработчик инструкции и помощи"""
from aiogram import Router, F
from aiogram.filters import Command
from aiogram.types import Message

router = Router()

INSTRUCTION_TEXT = (
    "📖 <b>Как пользоваться ботом</b>\n\n"

    "<b>Что делает бот:</b>\n"
    "Мониторит крупные Telegram-чаты с фрилансерскими вакансиями и "
    "мгновенно отправляет вам только те, которые соответствуют вашим профессиям.\n\n"

    "<b>Начало работы:</b>\n"
    "1. Нажмите /start и выберите свои профессии\n"
    "2. Готово — вакансии начнут приходить автоматически\n\n"

    "<b>Что мониторится:</b>\n"
    "Несколько крупных чатов с вакансиями для фрилансеров "
    "(дизайнеры, таргетологи, SMM, копирайтеры, видеомонтажёры и др.)\n\n"

    "<b>Профессии:</b>\n"
    "Можно выбрать несколько. Чем больше выбрано — тем больше вакансий получите. "
    "Изменить выбор можно через кнопку «🔍 Профессии» или /professions.\n\n"

    "<b>Подписка:</b>\n"
    "• Первые 7 дней — бесплатно\n"
    "• Далее: от 740₽/неделю\n"
    "• Оплата: Telegram Stars или банковская карта\n"
    "• Кнопка «💎 Подписка» или /subscribe\n\n"

    "<b>Как выглядит вакансия:</b>\n"
    "Вы получите сообщение с текстом вакансии, тегами профессий "
    "и кнопкой «Откликнуться» (ссылка на оригинальное сообщение).\n\n"

    "<b>Навигация (меню внизу):</b>\n"
    "👤 Профиль — статус подписки\n"
    "🔍 Профессии — сменить профессии\n"
    "💎 Подписка — тарифы и оплата\n"
    "📖 Инструкция — вы здесь\n\n"

    "<b>Вопросы?</b> Напишите администратору."
)


@router.message(Command("help"))
async def cmd_help(message: Message):
    await message.answer(INSTRUCTION_TEXT, parse_mode="HTML")


@router.message(F.text == "📖 Инструкция")
async def btn_help(message: Message):
    await message.answer(INSTRUCTION_TEXT, parse_mode="HTML")
